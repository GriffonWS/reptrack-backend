// Change Password
export const changePassword = async (req, res) => {
  try {
    const token = req.token;
    const gymOwnerId = req.gymOwner.id;
    const { oldPassword, newPassword, confirmPassword } = req.body;

    // Validate required fields
    if (!oldPassword || !newPassword || !confirmPassword) {
      return res.status(400).json({
        success: false,
        message: "All password fields are required",
        data: null,
      });
    }

    // Validate new password and confirm password match
    if (newPassword !== confirmPassword) {
      return res.status(400).json({
        success: false,
        message: "New password and confirm password do not match",
        data: null,
      });
    }

    // Find gym owner
    const gymOwner = await GymOwner.findOne({
      where: {
        id: gymOwnerId,
        token: token,
      },
    });

    if (!gymOwner) {
      return res.status(404).json({
        success: false,
        message: "Gym owner not found or invalid token",
        data: null,
      });
    }

    // Verify old password
    const isOldPasswordValid = await bcrypt.compare(
      oldPassword,
      gymOwner.password
    );
    if (!isOldPasswordValid) {
      return res.status(400).json({
        success: false,
        message: "Old password is incorrect",
        data: null,
      });
    }

    // Hash new password
    const hashedPassword = await bcrypt.hash(newPassword, 10);
    gymOwner.password = hashedPassword;
    await gymOwner.save();

    // Remove password from response
    const gymOwnerData = gymOwner.toJSON();
    delete gymOwnerData.password;

    return res.status(200).json({
      success: true,
      message: "Password changed successfully",
      data: gymOwnerData,
    });
  } catch (error) {
    console.error("Change password error:", error);
    return res.status(500).json({
      success: false,
      message: error.message || "Internal server error",
      data: null,
    });
  }
};

